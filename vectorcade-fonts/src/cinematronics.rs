use glam::Vec2;
use vectorcade_shared::font::{FontStyleId, GlyphPath, GlyphPathCmd, VectorFont};

pub struct Cinematronics;

impl VectorFont for Cinematronics {
    fn style_id(&self) -> FontStyleId { FontStyleId::CINEMATRONICS }
    fn has_glyph(&self, ch: char) -> bool { matches!(ch, '0'..='9' | 'A'..='Z' | ' ' | '.' | ',' | ':' | '-') }
    fn glyph_paths(&self, ch: char) -> Vec<GlyphPath> { glyph(ch) }
    fn advance(&self, _ch: char) -> f32 { 0.85 }
}

fn glyph(ch: char) -> Vec<GlyphPath> {
    match ch {
        '0'..='9' => vec![build(DIGITS[(ch as u8 - b'0') as usize])],
        'A'..='Z' => vec![build(LETTERS[(ch as u8 - b'A') as usize])],
        ' ' => vec![],
        '.' => vec![GlyphPath { cmds: vec![
            GlyphPathCmd::MoveTo(Vec2::new(0.4, 0.05)), GlyphPathCmd::LineTo(Vec2::new(0.5, 0.15)),
            GlyphPathCmd::LineTo(Vec2::new(0.6, 0.05)), GlyphPathCmd::LineTo(Vec2::new(0.5, 0.0)), GlyphPathCmd::Close,
        ]}],
        ',' => vec![GlyphPath { cmds: vec![GlyphPathCmd::MoveTo(Vec2::new(0.5, 0.1)), GlyphPathCmd::LineTo(Vec2::new(0.3, -0.1))] }],
        ':' => colon_diamonds(),
        '-' => vec![GlyphPath { cmds: vec![GlyphPathCmd::MoveTo(Vec2::new(0.2, 0.5)), GlyphPathCmd::LineTo(Vec2::new(0.8, 0.5))] }],
        _ => vec![],
    }
}

fn colon_diamonds() -> Vec<GlyphPath> {
    let diamond = |y: f32| GlyphPath { cmds: vec![
        GlyphPathCmd::MoveTo(Vec2::new(0.5, y)), GlyphPathCmd::LineTo(Vec2::new(0.6, y + 0.05)),
        GlyphPathCmd::LineTo(Vec2::new(0.5, y + 0.1)), GlyphPathCmd::LineTo(Vec2::new(0.4, y + 0.05)), GlyphPathCmd::Close,
    ]};
    vec![diamond(0.2), diamond(0.6)]
}

fn build(data: &[(bool, f32, f32)]) -> GlyphPath {
    GlyphPath { cmds: data.iter().map(|&(m, x, y)| {
        if m { GlyphPathCmd::MoveTo(Vec2::new(x, y)) } else { GlyphPathCmd::LineTo(Vec2::new(x, y)) }
    }).collect() }
}

